# Введення розміру масиву
n = int(input("n = "))

# Введення елементів масиву з клавіатури
print(f"Введіть {n} елементів масиву:")
arr = [int(input()) for _ in range(n)]

# Виведення початкового масиву
print("Початковий масив:", arr)

# Знаходження максимального додатного елемента
positive = [x for x in arr if x > 0]

if positive:
    maximum = max(positive)
    print("Максимальний додатний елемент:", maximum)
else:
    print("У масиві немає додатних елементів.")